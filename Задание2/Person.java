package Задание2;
class Person {
    String name;
    String position;
    String email;
    String phoneNumber;
    int salary;
    int age;

    public Person(String name, String position, String email, String phoneNumber, int salary, int age) {
        this.name = name;
        this.position = position;
        this.email = email;
        this.phoneNumber = phoneNumber;
        this.salary = salary;
        this.age = age;
    }
}

